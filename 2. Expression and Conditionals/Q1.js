let a=10;
let b=3;

// Arithmetic Operators

console.log("a + b = ",a+b)
console.log("a - b = ",a-b)
console.log("a * b = ",a*b)
console.log("a / b = ",a/b)
console.log("a % b = ",a%b)
console.log("a ^ b = ",a**b)
console.log("a++ = ",a++)
console.log("++a = ",++a)
console.log("a++ = ",--a)
console.log("++a = ",a--)

// Comparison Operator

let name="Mayank is my name"

// to print the length of the string
console.log(name.length);

// to convert all letter to uppercase
console.log(name.toUpperCase());

// to convert all letters to lowercase
console.log(name.toLowerCase());

// to slice the string 
console.log(name.slice(1,4));
console.log(name.slice(1));

// to replace a word form the string
console.log(name.replace("name","Boy"));

// to concat a multiple strings
let a="Ram";
console.log(name.concat(".He is a boy. ",a," is also a boy."))

// to remove the space from start and end
let res="  Meena   "
console.log(res.trim())
